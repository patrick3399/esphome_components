// ---------- color utilities ----------
uint32_t color_blend(uint32_t c1, uint32_t c2, uint8_t blend) {
  if (blend == 0)
    return c1;
  if (blend == 255)
    return c2;
  uint8_t r = lerp8by8(R(c1), R(c2), blend);
  uint8_t g = lerp8by8(G(c1), G(c2), blend);
  uint8_t b = lerp8by8(B(c1), B(c2), blend);
  uint8_t w = lerp8by8(W(c1), W(c2), blend);
  return RGBW32(r, g, b, w);
}

uint32_t color_fade(uint32_t c, uint8_t amount, bool video) {
  if (video) {
    return RGBW32(scale8_video(R(c), amount), scale8_video(G(c), amount), scale8_video(B(c), amount),
                  scale8_video(W(c), amount));
  }
  return RGBW32(scale8(R(c), amount), scale8(G(c), amount), scale8(B(c), amount), scale8(W(c), amount));
}

uint32_t color_add(uint32_t c1, uint32_t c2) {
  return RGBW32(qadd8(R(c1), R(c2)), qadd8(G(c1), G(c2)), qadd8(B(c1), B(c2)), qadd8(W(c1), W(c2)));
}

uint32_t color_wheel(uint8_t pos) {
  pos = 255 - pos;
  if (pos < 85)
    return RGBW32(255 - pos * 3, 0, pos * 3);
  if (pos < 170) {
    pos -= 85;
    return RGBW32(0, pos * 3, 255 - pos * 3);
  }
  pos -= 170;
  return RGBW32(pos * 3, 255 - pos * 3, 0);
}

